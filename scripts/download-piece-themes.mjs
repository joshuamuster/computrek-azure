/**
 * download-piece-themes.mjs
 *
 * Fetches chess piece SVGs from lichess's open-source repo and writes:
 *   public/chess-themes/{theme}/{piece}.svg   — individual SVG files (used as <img> previews)
 *   public/chess-themes/{theme}.css           — Chessground-compatible stylesheet
 *
 * Run once (or whenever you want to update sets):
 *   node scripts/download-piece-themes.mjs
 */

import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..')

const BASE_URL = 'https://raw.githubusercontent.com/lichess-org/lila/master/public/piece'

// Themes to download (cburnett is already bundled via npm — we only grab its queen for preview)
const DOWNLOAD_THEMES = ['merida', 'california', 'spatial', 'maestro']

// All 12 piece codes used by lichess
const PIECES = ['wK', 'wQ', 'wR', 'wB', 'wN', 'wP', 'bK', 'bQ', 'bR', 'bB', 'bN', 'bP']

// Map lichess filenames → Chessground CSS class names
function toCssSelector(pieceCode) {
  const colorChar = pieceCode[0]
  const typeChar  = pieceCode[1]
  const color = colorChar === 'w' ? 'white' : 'black'
  const type = { K: 'king', Q: 'queen', R: 'rook', B: 'bishop', N: 'knight', P: 'pawn' }[typeChar]
  return { color, type }
}

async function fetchSVG(url) {
  const res = await fetch(url)
  if (!res.ok) throw new Error(`HTTP ${res.status} — ${url}`)
  return res.text()
}

async function buildTheme(theme) {
  const dir = path.join(ROOT, 'public', 'chess-themes', theme)
  fs.mkdirSync(dir, { recursive: true })

  const cssLines = [`/* ${theme} piece set — generated by download-piece-themes.mjs */`]

  for (const piece of PIECES) {
    const url = `${BASE_URL}/${theme}/${piece}.svg`
    process.stdout.write(`  fetching ${theme}/${piece}.svg … `)

    const svg = await fetchSVG(url)
    fs.writeFileSync(path.join(dir, `${piece}.svg`), svg, 'utf8')

    const { color, type } = toCssSelector(piece)
    // Reference the file by public URL so no base64 bloat, and the same file
    // serves double-duty as the <img> preview in the picker modal.
    cssLines.push(`.cg-wrap piece.${type}.${color} { background-image: url('/chess-themes/${theme}/${piece}.svg') !important; }`)
    console.log('✓')
  }

  const cssPath = path.join(ROOT, 'public', 'chess-themes', `${theme}.css`)
  fs.writeFileSync(cssPath, cssLines.join('\n') + '\n', 'utf8')
  console.log(`  → wrote ${path.relative(ROOT, cssPath)}\n`)
}

// Also grab just the cburnett queen for the picker preview
async function grabCburnettPreview() {
  const dir = path.join(ROOT, 'public', 'chess-themes', 'cburnett')
  fs.mkdirSync(dir, { recursive: true })
  const previewPath = path.join(dir, 'wQ.svg')
  if (fs.existsSync(previewPath)) {
    console.log('  cburnett/wQ.svg already exists — skipping\n')
    return
  }
  process.stdout.write('  fetching cburnett/wQ.svg … ')
  const svg = await fetchSVG(`${BASE_URL}/cburnett/wQ.svg`)
  fs.writeFileSync(previewPath, svg, 'utf8')
  console.log('✓\n')
}

console.log('Downloading chess piece themes from lichess…\n')

try {
  await grabCburnettPreview()
  for (const theme of DOWNLOAD_THEMES) {
    console.log(`[${theme}]`)
    await buildTheme(theme)
  }
  console.log('All done! Files written to public/chess-themes/')
} catch (err) {
  console.error('\nFailed:', err.message)
  process.exit(1)
}
